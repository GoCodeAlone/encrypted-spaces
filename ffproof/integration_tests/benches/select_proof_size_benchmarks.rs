// ─── Stdio suppression (bench-local) ────────────────────────────────────────

/// Redirect stdout/stderr to `/dev/null` while the guard is alive.
///
/// Used only for high-iteration benchmark setup loops to hide repetitive
/// dev-mode warning output.
struct SuppressStdio {
    saved_stdout: i32,
    saved_stderr: i32,
}

impl SuppressStdio {
    fn new() -> Self {
        let saved_stdout = unsafe { libc::dup(1) };
        let saved_stderr = unsafe { libc::dup(2) };
        let devnull = std::fs::File::open("/dev/null").expect("open /dev/null");
        unsafe {
            libc::dup2(devnull.as_raw_fd(), 1);
            libc::dup2(devnull.as_raw_fd(), 2);
        }
        Self {
            saved_stdout,
            saved_stderr,
        }
    }
}

impl Drop for SuppressStdio {
    fn drop(&mut self) {
        unsafe {
            libc::dup2(self.saved_stdout, 1);
            libc::dup2(self.saved_stderr, 2);
            libc::close(self.saved_stdout);
            libc::close(self.saved_stderr);
        }
    }
}

// ─── Custom Criterion Measurement: Proof bytes ───────────────────────────────

struct ProofBytes;

struct BytesFormatter;

impl ValueFormatter for BytesFormatter {
    fn format_value(&self, value: f64) -> String {
        if value >= 1_048_576.0 {
            format!("{:.2} MiB", value / 1_048_576.0)
        } else if value >= 1024.0 {
            format!("{:.2} KiB", value / 1024.0)
        } else {
            format!("{:.0} B", value)
        }
    }

    fn format_throughput(&self, throughput: &Throughput, value: f64) -> String {
        match throughput {
            Throughput::Elements(n) => format!("{:.0} B/elem", value / *n as f64),
            Throughput::Bytes(n) | Throughput::BytesDecimal(n) => {
                format!("{:.2} ratio", value / *n as f64)
            }
        }
    }

    fn scale_values(&self, _typical_value: f64, _values: &mut [f64]) -> &'static str {
        "B"
    }

    fn scale_throughputs(
        &self,
        _typical_value: f64,
        _throughput: &Throughput,
        _values: &mut [f64],
    ) -> &'static str {
        "B/elem"
    }

    fn scale_for_machines(&self, _values: &mut [f64]) -> &'static str {
        "B"
    }
}

impl Measurement for ProofBytes {
    type Intermediate = ();
    type Value = u64;

    fn start(&self) -> Self::Intermediate {}
    fn end(&self, _i: Self::Intermediate) -> Self::Value {
        0
    }
    fn add(&self, v1: &Self::Value, v2: &Self::Value) -> Self::Value {
        v1 + v2
    }
    fn zero(&self) -> Self::Value {
        0
    }
    fn to_f64(&self, value: &Self::Value) -> f64 {
        *value as f64
    }
    fn formatter(&self) -> &dyn ValueFormatter {
        &BytesFormatter
    }
}

// ─── Bench parameters ────────────────────────────────────────────────────────

/// Number of `authors` rows to seed.
const NUM_AUTHORS: usize = 20;

/// Number of `posts` rows to seed. Each post is assigned a round-robin
/// `author_id` in `1..=NUM_AUTHORS`, so the secondary-index scan for
/// any one author returns roughly `NUM_POSTS / NUM_AUTHORS` rows.
const NUM_POSTS: usize = 500;

// ─── Schema ──────────────────────────────────────────────────────────────────

#[derive(Debug, Serialize, Deserialize)]
struct Author {
    id: Option<i64>,
    name: String,
}

#[derive(Debug, Serialize, Deserialize)]
struct Post {
    id: Option<i64>,
    author_id: i64,
    title: String,
}

fn schemas() -> Vec<encrypted_spaces_backend::schema::Schema> {
    let authors = SchemaBuilder::new("authors")
        .column("id", ColumnType::Integer)
        .plaintext_primary_key()
        .column("name", ColumnType::String)
        .unwrap()
        .build()
        .unwrap();

    let posts = SchemaBuilder::new("posts")
        .column("id", ColumnType::Integer)
        .plaintext_primary_key()
        .column("author_id", ColumnType::Integer)
        .unwrap()
        .plaintext()
        .index()
        .column("title", ColumnType::String)
        .unwrap()
        .build()
        .unwrap();

    vec![authors, posts]
}

// ─── Bench ops ───────────────────────────────────────────────────────────────

#[derive(Clone, Copy)]
enum BenchSelect {
    PkLookup,
    IndexedLookup,
    PkJoin,
    IndexedJoin,
}

impl BenchSelect {
    const ALL: &'static [BenchSelect] = &[
        BenchSelect::PkLookup,
        BenchSelect::IndexedLookup,
        BenchSelect::PkJoin,
        BenchSelect::IndexedJoin,
    ];

    fn label(self) -> &'static str {
        match self {
            BenchSelect::PkLookup => "pk_lookup",
            BenchSelect::IndexedLookup => "indexed_lookup",
            BenchSelect::PkJoin => "pk_join",
            BenchSelect::IndexedJoin => "indexed_join",
        }
    }

    /// Build the `Query` to measure. Predicate values are picked to
    /// land in the middle of the seeded id space so neither end of the
    /// merk tree is special-cased.
    fn query(self) -> Query {
        let mid_post: i64 = (NUM_POSTS as i64) / 2;
        let mid_author: i64 = (NUM_AUTHORS as i64) / 2;

        let join = JoinClause {
            table: "authors".to_string(),
            on_condition: ("author_id".to_string(), "id".to_string()),
        };
        let pk_eq = |col: &str, v: i64| Predicate {
            column: col.to_string(),
            operator: ComparisonOperator::Equal,
            values: vec![QueryParam::Integer(v)],
            cursor_id: None,
        };

        match self {
            BenchSelect::PkLookup => Query {
                table: "posts".to_string(),
                operation: QueryOperation::Select(Vec::new()),
                predicate: Some(pk_eq("id", mid_post)),
                join: None,
                order: Order::Asc,
                limit: None,
            },
            BenchSelect::IndexedLookup => Query {
                table: "posts".to_string(),
                operation: QueryOperation::Select(Vec::new()),
                predicate: Some(pk_eq("author_id", mid_author)),
                join: None,
                order: Order::Asc,
                limit: None,
            },
            BenchSelect::PkJoin => Query {
                table: "posts".to_string(),
                operation: QueryOperation::Select(Vec::new()),
                predicate: Some(pk_eq("id", mid_post)),
                join: Some(join),
                order: Order::Asc,
                limit: None,
            },
            BenchSelect::IndexedJoin => Query {
                table: "posts".to_string(),
                operation: QueryOperation::Select(Vec::new()),
                predicate: Some(pk_eq("author_id", mid_author)),
                join: Some(join),
                order: Order::Asc,
                limit: None,
            },
        }
    }
}

// ─── Fixture ─────────────────────────────────────────────────────────────────

struct Fixture {
    sizes: HashMap<&'static str, usize>,
}

fn fixture() -> &'static Fixture {
    static FIXTURE: OnceLock<Fixture> = OnceLock::new();
    FIXTURE.get_or_init(|| {
        let rt = tokio::runtime::Runtime::new().unwrap();
        rt.block_on(build_fixture())
    })
}

async fn build_fixture() -> Fixture {
    let t0 = std::time::Instant::now();

    let schemas = schemas();
    let transport = LocalTransport::new(&schemas, None, None)
        .await
        .expect("LocalTransport::new");
    let app_root = transport.get_root_hash().await.expect("root");
    let app_schema = ApplicationSchema::for_testing(schemas.clone(), app_root);

    eprintln!("[setup] Space::create");
    // Hold a handle to the inner transport so we can call
    // `select_proof_bytes` after `Space::create` consumes ownership.
    let transport_for_proofs = transport.clone();
    let space = Space::create(transport, app_schema)
        .await
        .expect("Space::create");

    // ─── Seed authors ─────────────────────────────────────────────────────
    eprintln!("[setup] inserting {NUM_AUTHORS} authors");
    let authors = space.table::<Author>("authors");
    for i in 0..NUM_AUTHORS {
        {
            let _quiet = SuppressStdio::new();
            authors
                .insert(&Author {
                    id: None,
                    name: format!("Author{i}"),
                })
                .execute()
                .await
                .expect("author insert execute");
        }
    }

    // ─── Seed posts (round-robin author_id over 1..=NUM_AUTHORS) ──────────
    eprintln!("[setup] inserting {NUM_POSTS} posts");
    let posts = space.table::<Post>("posts");
    for i in 0..NUM_POSTS {
        let author_id = (i % NUM_AUTHORS) as i64 + 1;
        {
            let _quiet = SuppressStdio::new();
            posts
                .insert(&Post {
                    id: None,
                    author_id,
                    title: format!("Post{i}"),
                })
                .execute()
                .await
                .expect("post insert execute");
        }
        if (i + 1) % 100 == 0 || i + 1 == NUM_POSTS {
            eprintln!(
                "  [setup] {}/{} posts inserted ({:.2?})",
                i + 1,
                NUM_POSTS,
                t0.elapsed()
            );
        }
    }

    // ─── Generate + verify a proof per query ──────────────────────────────
    // Reuse the post-seed root as the verification commitment — no
    // writes happen between here and the proof generation below.
    let commitment = transport_for_proofs.get_root_hash().await.expect("root");
    let mut sizes: HashMap<&'static str, usize> = HashMap::new();
    let mut row_counts: HashMap<&'static str, (usize, usize)> = HashMap::new();
    let mut row_payload_bytes: HashMap<&'static str, usize> = HashMap::new();
    for op in BenchSelect::ALL {
        let query = op.query();
        let proof = transport_for_proofs
            .select_proof_bytes(&query)
            .await
            .unwrap_or_else(|e| panic!("select_proof_bytes for {}: {e:?}", op.label()));
        // Verify and capture the returned rows so we can report what
        // fraction of the proof bytes is actual row payload vs.
        // verification overhead.
        let verified: VerifiedRows = verify_query_proof(&query, &proof, &commitment)
            .unwrap_or_else(|e| panic!("verify_query_proof for {}: {e:?}", op.label()));
        let main_n = verified.main_rows.len();
        let joined_n: usize = verified.rows_by_table.values().map(|v| v.len()).sum();
        // Postcard-encode the rows the verifier exposes to the client.
        // This is a lower bound on "data-only" bytes — i.e. the
        // payload a non-verifying transport could ship. The proof has
        // to additionally cover merk path nodes, hashes, etc.
        let mut data_only = 0usize;
        for r in &verified.main_rows {
            data_only += postcard::to_allocvec(r).map(|v| v.len()).unwrap_or(0);
        }
        for rows in verified.rows_by_table.values() {
            for r in rows {
                data_only += postcard::to_allocvec(r).map(|v| v.len()).unwrap_or(0);
            }
        }
        sizes.insert(op.label(), proof.len());
        row_counts.insert(op.label(), (main_n, joined_n));
        row_payload_bytes.insert(op.label(), data_only);
    }

    // ─── Whole-DB size: sum of postcard-encoded rows in seeded tables ─────
    // We probe the DB via a no-predicate select for each user table and
    // sum the resulting row bytes. This isn't the merk on-disk size —
    // it's the application-level row payload total, which is the right
    // baseline to compare proof bytes against.
    let mut db_proof_bytes = 0usize;
    let mut db_row_payload_bytes = 0usize;
    let mut db_row_count = 0usize;
    for table in ["authors", "posts"] {
        let q = Query {
            table: table.to_string(),
            operation: QueryOperation::Select(Vec::new()),
            predicate: None,
            join: None,
            order: Order::Asc,
            limit: None,
        };
        let p = transport_for_proofs
            .select_proof_bytes(&q)
            .await
            .unwrap_or_else(|e| panic!("scan {table}: {e:?}"));
        db_proof_bytes += p.len();
        let v = verify_query_proof(&q, &p, &commitment)
            .unwrap_or_else(|e| panic!("verify scan {table}: {e:?}"));
        db_row_count += v.main_rows.len();
        for r in &v.main_rows {
            db_row_payload_bytes += postcard::to_allocvec(r).map(|x| x.len()).unwrap_or(0);
        }
    }

    eprintln!("[setup] DONE in {:.2?}.", t0.elapsed());
    eprintln!(
        "[setup] DB: {} rows total ({} authors + {} posts), {} B postcard row payload, \
         {} B for full-table scan proofs (both tables combined)",
        db_row_count, NUM_AUTHORS, NUM_POSTS, db_row_payload_bytes, db_proof_bytes,
    );
    eprintln!(
        "[setup] {:>16}  {:>10}  {:>14}  {:>10}  {:>11}  {:>9}",
        "query", "proof B", "rows (main+jn)", "data B", "overhead B", "ratio",
    );
    for op in BenchSelect::ALL {
        let label = op.label();
        let proof = sizes[label];
        let (main_n, jn_n) = row_counts[label];
        let data = row_payload_bytes[label];
        let overhead = proof as i64 - data as i64;
        let ratio = if data == 0 {
            f64::INFINITY
        } else {
            proof as f64 / data as f64
        };
        eprintln!(
            "[setup] {:>16}  {:>10}  {:>6}+{:<7}  {:>10}  {:>11}  {:>8.2}x",
            label, proof, main_n, jn_n, data, overhead, ratio,
        );
    }

    Fixture { sizes }
}

// ─── Benchmarks ──────────────────────────────────────────────────────────────

fn bench_all_selects(c: &mut Criterion<ProofBytes>) {
    for op in BenchSelect::ALL {
        let label = op.label();
        let bench_name = format!("select_proof_size/{label}_A{NUM_AUTHORS}_P{NUM_POSTS}");
        c.bench_function(&bench_name, |b| {
            b.iter_custom(|iters| {
                let n = fixture()
                    .sizes
                    .get(label)
                    .copied()
                    .unwrap_or_else(|| panic!("query '{label}' was not recorded during setup"));
                // 1ms pause per sample so Criterion doesn't pick `iters`
                // in the billions and underflow the reported per-iter
                // byte count to zero. See `update_proof_size_benchmarks`
                // for the same workaround. Note: the *first* bench in
                // the run pays an outsized warmup cost (~1min), same as
                // the first bench in the update-proof bench — Criterion
                // appears to use a different ramp for the first
                // benched function. Subsequent benches finish in ~10ms.
                let sleep_ms = iters.min(2);
                std::thread::sleep(std::time::Duration::from_millis(sleep_ms));
                n as u64 * iters
            });
        });
    }
}

// ─── Criterion plumbing ──────────────────────────────────────────────────────

fn proof_size_criterion() -> Criterion<ProofBytes> {
    // Bench-local env tuning: keep proof-size output readable.
    std::env::set_var("RISC0_DEV_MODE", "1");
    std::env::remove_var("RISC0_INFO");
    std::env::set_var("RUST_LOG", "error");
    std::env::set_var("RISC0_GUEST_LOGFILE", "/dev/null");

    Criterion::default()
        .with_measurement(ProofBytes)
        .sample_size(10)
        .nresamples(10)
        .warm_up_time(std::time::Duration::from_millis(1))
        .measurement_time(std::time::Duration::from_millis(1))
        .significance_level(0.0001)
        .noise_threshold(1.0)
}

criterion_group! {
    name = select_proof_size_benchmarks;
    config = proof_size_criterion();
    targets = bench_all_selects
}

criterion_main!(select_proof_size_benchmarks);
