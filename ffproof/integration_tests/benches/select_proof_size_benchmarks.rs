//! Size benchmarks for **SELECT query proofs** returned by the server
//! to a client running a `SELECT`. Each query gets a single Merk
//! proof (or tracer-based proof, for indexed predicates / joins)
//! covering all rows it returns; this bench measures that proof byte
//! length under a small matrix of query shapes:
//!
//!   - `pk_lookup`     — `posts WHERE id = ?`
//!   - `indexed_lookup` — `posts WHERE author_id = ?`        (secondary index)
//!   - `pk_join`       — `posts WHERE id = ? JOIN authors`   (FK→PK)
//!   - `indexed_join`  — `posts WHERE author_id = ? JOIN authors`
//!
//! The schema is intentionally demo-shaped — two related tables with
//! a primary key on each side and one secondary index on the foreign
//! key column — so the four queries cover {PK, indexed} × {standalone,
//! joined}.
//!
//! Proofs are generated by calling
//! [`LocalTransport::select_proof_bytes`] directly (which returns the
//! raw proof bytes without verifying), so this bench measures the
//! exact bytes the server would put on the wire as
//! `SelectResponse.proof`. Each captured proof is also verified once
//! during setup as a sanity check.
//!
//! Run with:
//!   cargo bench -p encrypted-spaces-ff-test --bench select_proof_size_benchmarks
//!
//! Sizes are deterministic for a given input, so the Criterion bench
//! body just replays the recorded byte count under a custom
//! `ProofBytes` measurement (same pattern as
//! `update_proof_size_benchmarks.rs`).

